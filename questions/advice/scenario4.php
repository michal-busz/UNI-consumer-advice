<h1>Scenario 4</h1>
<p>For the <b>first 2 months</b>.
You only have consumer rights if the item isn’t as described on the advert. The product should be true to what was advertised and what was said by the seller.<br>
If the item is not as described, you are entitled to a refund if you have not personalised the item since you brought it.<br>
You are only entitled to a refund if there is something wrong with the item, within a reasonable time. Unfortunately, the law is not specific on what is a ‘reasonable’ time frame.</p>