<p>You must prove that the car was faulty when you first brought it and the trader must pay for the repair.<br>
As a consumer, you have legal rights up to 6 years, but this is only the case if you can prove that you did not cause the fault. It should be noted that the longer you have the car, the harder it will be to prove you did not cause the fault.<br>
A faulty car means it is not:
<ul>
    <li>of satisfactory quality</li>
    <li>fit for purpose</li>
    <li>as described</li>
</ul>
You should ask for a second opinion before claiming for a faulty good.</p>