<h1> Scenario 6</h1>
You are legally entitled to receive your money back or have a repair if the car is faulty.<br>

A faulty car means it is not:<br>

•of satisfactory quality<br>

•fit for purpose<br>

•as described<br>

You need to consider whether there are bigger issues with the car than just this faultiness. If you are not too worried, you can simply as for a discounted price. If the car is not driveable, ask the seller to collect the car.<br>

If you have part-exchanged, perhaps as for your old car back if you are requesting a refund. If the trader has sold on your car, you should ask for the value they gave you previously.