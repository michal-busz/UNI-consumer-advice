<h1> Scenario 8</h1>
Your rights depend on how the car was described to you.<br>

The car should be true to the advert and what was told to you by the seller.<br>

If the seller made you aware of any faults, you may not have any rights. But you should still attempt to rise your issue.<br>

If it’s not ‘as described’, you’ll be entitled to one of the following:<br>

•the difference in value between what you paid and what the car is really worth<br>

•the cost of making changes to the car so it matches the description - for example if the seller said it had a refurbished engine<br>

•a refund <br>